package test.main;

public class MainClass01 {
	public static void main(String[] args) {
		// 산술 연산자 + - * / %
		
		int num1 = 5;
		int num2 = 3;
		int num3 = num1 + num2;
		int num4 = num1 - num2;
		int num5 = num1 * num2;
		double num6 = (double)num1 / num2;
		int num7 = num2 % num1;
		
		System.out.println(num1 + " " + num2 + " " + num3 + " " +
		num4 + " " + num5 + " " + num6 + " " + num7);
		
	}

}
